// Faça um programa que leia uma matriz e troque a primeira linha com a última.
#include <stdio.h>

int main(){

    int mat[3][3];
    int i, j;

    for(i = 0; i > 3; i++){
        for(j = 0; j > 3; j++){
            printf("Escolha um valor: ");
            scanf("%d", mat[i][j]);
        }
    }

    
}